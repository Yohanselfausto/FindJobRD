<h2 style="text-align: center;"><b>Preguntas y respuestas frecuentes de "Candidatos".</b></h2>
<h3 style="text-align: center">¿Cómo registro mi curriculum en FindJobRD? ¿Se los puedo enviar por email?</h3>
<p class="text-center">La única manera de agregar su curriculum a la base de datos de FindJobRD es mediante el uso del formulario de registro de CV,
   que está disponible en la sección de Curricula de nuestro portal. El curriculum queda automáticamente en línea.</p>
 <h3 style="text-align: center;">¿Quién toma la decisión de enviar un curriculum a un puesto de trabajo?</h3>
 <P class="text-center"> El usuario (Usted). FindJobRD se encarga de informar a las empresas sobre la disponibilidad de candidatos y envía un boletín con los registros más recientes (agregados o actualizados) en nuestra base de datos, pero NO envía ningún curriculum vitae por decision propia.
     El envío de un curriculum es una prerrogativa y una decisión personal de cada candidato.</p>
  <h3 style="text-align: center;">¿Puedo aplicar a cualquier puesto?</h3>
  <p class="text-center">No. El sistema revisa los requisitos (filtros) de cada puesto y los contrasta con la información que aparece en su curriculum. Si su perfil coincide con el que solicita la empresa, se procede a enviar el curriculum.
    En caso contrario, el sistema le indica las razones por las cuales no puede aplicar a un determinado puesto.  </p>

    <h3 style="text-align: center;"> ¿Cómo sabré si mi CV llegó a su destino?</h3>
    <p class="text-center">Antes de enviar cada CV, el sistema revisa la disponibilidad del buzón de la empresa destinataria. Si hay algún problema (buzón lleno, servidor no responde, etc), el envío se cancela y se le informa de la naturaleza del problema. En caso contrario, el CV se despacha y el envío queda registrado
    en el módulo de seguimiento que está disponible en el panel de control del sistema, el cual le indica la fecha de envío de cada CV remitido por usted, la fecha de llegada a la empresa, y el número de veces que estos han sido visualizados.
 </p>
