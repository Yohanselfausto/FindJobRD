<h2 style="text-align: center;"><b>Preguntas y respuestas frecuentes de "Servicios Opcionales".</b></h2>
<h3 style="text-align: center;"> ¿Por qué necesito una aprobación previa para contratar algunos servicios opcionales? </h3>
<p class="text-center">Para tener la seguridad de que le vamos a ofrecer los servicios opcionales que desea contratar.
   No quisiéramos que, por ejemplo, contrate espacio publicitario para un producto o servicio que podría estar en conflicto con la política de uso o filosofía del portal, o que requiera una preselección de candidatos para un puesto para el que no tenemos perfiles suficientes,
   o que contrate el envío o recepción de minimensajes para un teléfono móvil con cuyo proveedor de telefonía no trabajamos, etc.</p>

<h3 style="text-align: center;"> ¿Es posible pagar los servicios opcionales con cheque o mediante transferencia bancaria? </h3>
<p class="text-center">En principio no tenemos inconveniente en aceptar otras formas de pago siempre y cuando usted asuma todas las comisiones bancarias o financieras de la operación. Por otra parte, tendrá que esperar a que el pago se haya acreditado para recibir el servicio contratado.
   En caso de no poder ofrecerle el servicio (ver razones para una aprobación previa) y debamos devolverle el dinero, se le descontará cualquier comisión bancaria o financiera que se requiera para hacerle llegar por la misma vía el pago recibido.
   La seguridad, economía, rapidez y sencillez de la pasarela de pago con tarjeta de crédito o débito, es muy superior a la de cualquier otra forma de pago.</p>
