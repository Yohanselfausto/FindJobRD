<h2 style="text-align: center;"><b>Preguntas y respuestas frecuentes de "Empresas".</b></h2>
<h3 style="text-align: center;"> ¿Cuál es el precio de este servicio?</h3>
<p class="text-center">Las empresas pueden publicar su 1ra. oferta de empleo en forma gratuita (*) y gratuitamente pueden recibir todos los currículums que los candidatos
   envíen a dichos puestos. Las demás publicaciones de empleo tienen un precio al que se le aplican descuentos por volúmen.</>

<h3 style="text-align: center;">¿Podemos colocar el logo de nuestra empresa?</h3>
<p class="text-center">Todos los anuncios pueden ir acompañados del logo de la empresa anunciante. El logo será visible únicamente si el nombre de la empresa también lo es, es decir, si el anuncio no se ha publicado en modo
   confidencial. El logo puede insertarse o eliminarse a través del panel de control o desde el mismo formulario de publicación de anuncios.</p>
<h3 style="text-align: center;">¿Qué tipo de ofertas de empleo NO son admitidas?</h3>
<p class="text-center"> Cualquier empleo que entre en contradicción con las Normas Internacionales del Trabajo definidas por la OIT.</p>
